const messages = { en, zh };

/* ------------------------------------------------------------------ */

type I18nValue = {
  lang: Lang;
  setLang: (l: Lang) => void;
  toggle: () => void;
  /** Dotted-path lookup that returns strings, arrays or objects. */
  t: (path: string) => any;
};

const I18nContext = createContext<I18nValue | null>(null);

function resolve(tree: any, path: string): any {
  return path.split(".").reduce((acc, key) => (acc == null ? acc : acc[key]), tree);
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  // Hydrate from storage after mount (avoids SSR mismatch).
  useEffect(() => {
    const stored = (typeof window !== "undefined" &&
      window.localStorage.getItem(STORAGE_KEY)) as Lang | null;
    if (stored === "en" || stored === "zh") setLangState(stored);
  }, []);

  useEffect(() => {
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang === "zh" ? "zh-Hans" : "en";
    }
  }, [lang]);

  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") window.localStorage.setItem(STORAGE_KEY, l);
  };

  const value: I18nValue = {
    lang,
    setLang,
    toggle: () => setLang(lang === "en" ? "zh" : "en"),
    t: (path: string) => {
      const hit = resolve(messages[lang], path);
      if (hit !== undefined) return hit;
      const fallback = resolve(messages.en, path);
      return fallback !== undefined ? fallback : path;
    },
  };

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within <I18nProvider>");
  return ctx;
}
